package dev.blackcat.porlalivre.fragments.internet;

public class UpdateInfo 
{
	
	String date;
	String frequency;
	String name;
	String totalUpdates;
	String size;
	String url;
	
	public UpdateInfo(String date, String frequency, String name, String totalUpdates, String size,	String url)
	{
		this.date = date;
		this.frequency = frequency;
		this.name = name;
		this.totalUpdates = totalUpdates;
		this.size = size;
		this.url = url;
	}

}
